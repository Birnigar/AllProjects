package day18;

public class ForLoopIntro {
    public static void main(String[] args) {
        //count 1 to 5

        for (int n=1;n<=5;++n){
            System.out.println("n = "+n);
            System.out.println("Happy Birthday");
        }
     for (int counter=5; counter>0;--counter){
         System.out.println("counter "+counter);
     }
    }
}
