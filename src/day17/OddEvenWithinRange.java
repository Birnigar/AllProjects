package day17;

public class OddEvenWithinRange {
    public static void main(String[] args) {
         int num=0;
         while (num<=50){
             if(num%2==0){
                 System.out.println(num+" is odd number");
             }else{
                 System.out.println((num+" is even number"));
             }
             ++num;
         }









    }
}
